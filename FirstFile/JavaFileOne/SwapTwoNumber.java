// package JavaFileOne;


public class SwapTwoNumber {
    public static void main(String[] args) {

        System.out.println("Swapping of two numbers :");

        int num1 = 10;
        int num2 = 20;

        int c = num1;
        num1 = num2;
        num2 = c;
        System.out.println(num1 + " , " + num2 );
    }
}